App::App()
    : App("App", 0)
{
}

App::App(const char *appName, int pages)
    : currentPage(0),
      nbPages(pages),
      name(appName)
{
}

int App::getCurrentPage()
{
    return this->currentPage;
}

bool App::nextPage()
{
    if (this->currentPage < nbPages)
    {
        this->currentPage++;
        return true;
    }
    return false;
}

bool App::previousPage()
{
    if (this->currentPage > 0)
    {
        this->currentPage--;
        return true;
    }
    return false;
}

